package Example;

public class SwitchStatementString {

	public static void main(String[] ar)
	{
		
	
	String fruit="Appl";
	
	switch(fruit) {
	case "Apple":
		System.out.println("You chose an Apple");
		break;
	
	case "Banana":
		System.out.println("You chose a Banana");
		break;
				
		default:
			System.out.println("Out of the Boundary");
	}
	}
}
