//                              Final Variable
                     // It's the Keyword which is used to implement in variable , method and class.
                     // At once it is final(assign)we can not change.
package Inheritancee;
class fnl
{
	final int x=10;
	void run()
	{
		//x=5;
		System.out.println("a ="+x);
	}
}
public class Final {
 public static void main(String s[])
 {
	 fnl n=new fnl();
	 n.run();
 }
}
